package day04_primitive_operators;

public class Primitives 
{
	public static void main(String [] args) 
	{
		byte byteValue         = 127;
		short shortValue       = 23000;
		int intValue           = 12345645;
		long longValue         = 124239862358658L;
		
		float floatValue       = 12.123F;
		double doubleValue     = 1231.5;
		
		char charValue         = '@';
		char charValue2        = 12345;
		
		boolean booleanValue   = true;
		boolean booleanValue2  = false;
		
	System.out.println("Byte: " + byteValue);
	System.out.println("Short: " + shortValue);
	System.out.println("Int: " + intValue);
	System.out.println("Long: " + longValue);
	
	System.out.println("Float: " + floatValue);
	System.out.println("Double: " + doubleValue);
	
	System.out.println("Char: " + charValue);
	System.out.println("Char2: " + charValue2);
	
	System.out.println("Boolean: " + booleanValue);
	System.out.println("Boolean2: " + booleanValue2);
		
		
	}
}
