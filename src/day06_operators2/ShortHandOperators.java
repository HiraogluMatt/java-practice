package day06_operators2;

public class ShortHandOperators {
	public static void main(String[] args) {
		int students = 45;
		System.out.println("Students: " + students);

		students = students + 5;
		System.out.println("Students: " + students);
		
		students = students - 2;
		System.out.println("Students: " + students);
		
		
	}

}
